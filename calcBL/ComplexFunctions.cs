using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace MyNamespace.CalcBL
{
    public class ComplexFunctions
    {
        public static double ReturnSin(double num1)
        {
            return Math.Sin(num1);
        }
        public static double ReturnCos(double num1)
        {
            return Math.Cos(num1);
        }
        public static double ReturnTan(double num1)
        {
            return Math.Tan(num1);
        }
    }
}
